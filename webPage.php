            <p>Key drivers of dropout</p><br>
    
            <img src="img/pic2.jpg" alt="img">
    
            <p>Age is a strong predictor of dropout. Older children (aged 13 to 18) enrolled in primary
                school are more likely to drop out compared to younger children. Dropout is a very rare
                event for children of primary school age (7 to 13 years of age). Children aged 13 and 
                above accounted for 88% of the cases of dropout in primary school in 2016- 2017. Dropout
                more than doubles for children between the ages of 13 and 14, from 4% to 11%, increasing to 
                about 16% at the age of 16 and 17. At 18 more than half of the children have dropped out of
                school.</p><br>
    
            <p> Poverty is correlated with dropout rates. There is a clear inverse correlation between household 
                wealth and dropout, or wealth and being out of school. Twelve years after the start of their 
                education an estimated 39% of children from the poorest 60% of households were still enrolled, 
                compared to over 60% of children from households in the wealthiest 40% of households. 
                Delayed start drives the out-of-school rate for young children. What drives the out-of-school rate for young 
                children is not dropout, but rather a delayed start to their education. Children who start 
                school late are more likely to drop out in the future, and thus less likely to transition from primary to secondary. 
                Delayed start to education explains ~95% of out-of-school cases for children between the ages of 7 to 9. Re-entry increases the risk of dropout.
            </p><br>
    
            <img src="img/pic3.jpg" alt="img">
    
            <p>Gender is a factor. Girls are much likely to be out of school from age 16 onwards, 
                due to lower reentry rates, as dropout is more permanent for girls than for boys.
                The main causes identified for this are: (i) girls reach Primary 6 before boys;
                (ii) girls are historically less likely to make the transition to secondary school;
                (iii) pregnancy and/or marriage are likely to be a dropout driver for females over
                18 still enrolled in school; (iv) girls are more likely to drop out due to
                unexpected household circumstances (i.e. death of a family member). Geography
                 plays a role. Dropout rates are much higher in rural areas than in cities. 
                This effect is seen only after the age of 13, i.e. after primary school age.</p><br>
    
